-- PROCEDIMIENTO PARA CONSULTAR HISTORIAL DE LECTURAS POR SENSOR Y RANGO DE FECHAS
CREATE PROCEDURE OBTENER_LECTURAS_SENSOR(
    IN P_ID_SENSOR INT,
    IN P_FECHA_INICIO DATETIME,
    IN P_FECHA_FIN DATETIME
)
BEGIN
    DECLARE V_EXISTE_SENSOR INT DEFAULT 0;
    
    -- VERIFICAR SI EL SENSOR EXISTE
    SELECT COUNT(*) INTO V_EXISTE_SENSOR 
    FROM SENSOR 
    WHERE ID_SENSOR = P_ID_SENSOR;
    
    IF V_EXISTE_SENSOR = 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'ERROR: EL SENSOR NO EXISTE';
    ELSE
        -- CONSULTAR LECTURAS EN EL RANGO DE FECHAS
        SELECT 
            L.ID_LECTURA,
            L.FK_ID_SENSOR,
            S.NOMBRE_SENSOR,
            L.VALOR_LECTURA,
            L.REGISTRADO_EN_LECTURA
        FROM LECTURA L
        INNER JOIN SENSOR S ON L.FK_ID_SENSOR = S.ID_SENSOR
        WHERE L.FK_ID_SENSOR = P_ID_SENSOR
        AND L.REGISTRADO_EN_LECTURA BETWEEN P_FECHA_INICIO AND P_FECHA_FIN
        ORDER BY L.REGISTRADO_EN_LECTURA DESC;
    END IF;
END //

