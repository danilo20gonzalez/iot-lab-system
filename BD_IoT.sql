-- Procedimiento para crear usuarios con hash de contraseña
CREATE PROCEDURE crear_usuario(
    IN p_username VARCHAR(50),
    IN p_password VARCHAR(255),
    IN p_email VARCHAR(100),
    IN p_id_rol INT
)
BEGIN
    DECLARE hashed_password VARCHAR(255);
    DECLARE user_count INT;
    
    -- Verificar si el usuario ya existe
    SELECT COUNT(*) INTO user_count 
    FROM USUARIO 
    WHERE USERNAME = p_username OR EMAIL = p_email;
    
    IF user_count > 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'El usuario o email ya existe';
    ELSE
        -- Generar hash de la contraseña (en producción usar bcrypt desde la aplicación)
        SET hashed_password = SHA2(CONCAT(p_password, 'salt_seguro'), 256);
        
        -- Insertar el nuevo usuario
        INSERT INTO USUARIO (USERNAME, PASSWORD, EMAIL, FK_ID_ROL)
        VALUES (p_username, hashed_password, p_email, p_id_rol);
        
        SELECT 'Usuario creado exitosamente' AS mensaje;
    END IF;
END //

-- Procedimiento para autenticar usuarios
CREATE PROCEDURE autenticar_usuario(
    IN p_username VARCHAR(50),
    IN p_password VARCHAR(255)
)
BEGIN
    DECLARE hashed_input VARCHAR(255);
    DECLARE user_id INT;
    DECLARE user_role INT;
    DECLARE user_email VARCHAR(100);
    DECLARE stored_hash VARCHAR(255);
    
    -- Generar hash de la contraseña ingresada
    SET hashed_input = SHA2(CONCAT(p_password, 'salt_seguro'), 256);
    
    -- Buscar usuario y verificar credenciales
    SELECT 
        u.ID_USUARIO,
        u.PASSWORD,
        u.EMAIL,
        u.FK_ID_ROL,
        r.NOMBRE_ROL
    INTO 
        user_id,
        stored_hash,
        user_email,
        user_role,
        @role_name
    FROM USUARIO u
    INNER JOIN ROL r ON u.FK_ID_ROL = r.ID_ROL
    WHERE u.USERNAME = p_username;
    
    IF user_id IS NULL THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'Usuario no encontrado';
    ELSEIF hashed_input != stored_hash THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'Contraseña incorrecta';
    ELSE
        -- Devolver información del usuario autenticado
        SELECT 
            user_id AS ID_USUARIO,
            p_username AS USERNAME,
            user_email AS EMAIL,
            user_role AS ID_ROL,
            @role_name AS ROL,
            'Autenticación exitosa' AS MENSAJE;
    END IF;
END //

-- Procedimiento para asignar usuarios a laboratorios
CREATE PROCEDURE asignar_usuario_laboratorio(
    IN p_id_usuario INT,
    IN p_id_LABORATORIO INT
)
BEGIN
    DECLARE user_exists INT;
    DECLARE modulo_exists INT;
    DECLARE assignment_exists INT;
    
    -- Verificar si el usuario existe
    SELECT COUNT(*) INTO user_exists 
    FROM USUARIO 
    WHERE ID_USUARIO = p_id_usuario;
    
    -- Verificar si el módulo existe
    SELECT COUNT(*) INTO modulo_exists 
    FROM LABORATORIO 
    WHERE ID_LABORATORIO = p_id_LABORATORIO;
    
    -- Verificar si la asignación ya existe
    SELECT COUNT(*) INTO assignment_exists 
    FROM USUARIO_LABORATORIO 
    WHERE FK_ID_USUARIO = p_id_usuario 
    AND FK_ID_LABORATORIO = p_id_LABORATORIO;
    
    IF user_exists = 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'El usuario no existe';
    ELSEIF modulo_exists = 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'El módulo de laboratorio no existe';
    ELSEIF assignment_exists > 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'El usuario ya está asignado a este laboratorio';
    ELSE
        -- Realizar la asignación
        INSERT INTO USUARIO_LABORATORIO (FK_ID_USUARIO, FK_ID_LABORATORIO)
        VALUES (p_id_usuario, p_id_LABORATORIO);
        
        SELECT 'Usuario asignado al laboratorio exitosamente' AS mensaje;
    END IF;
END //

