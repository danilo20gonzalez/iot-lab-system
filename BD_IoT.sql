-- PROCEDIMIENTO PARA OBTENER ALERTAS ACTIVAS DE UN LABORATORIO
CREATE PROCEDURE OBTENER_ALERTAS_ACTIVAS(
    IN P_ID_LABORATORIO INT
)
BEGIN
    DECLARE V_EXISTE_MODULO INT DEFAULT 0;
    
    -- VERIFICAR EXISTENCIA DEL MÓDULO
    SELECT COUNT(*) INTO V_EXISTE_MODULO 
    FROM LABORATORIO 
    WHERE ID_LABORATORIO = P_ID_LABORATORIO;
    
    IF V_EXISTE_MODULO = 0 THEN
        SIGNAL SQLSTATE '45000' 
        SET MESSAGE_TEXT = 'ERROR: EL MÓDULO DE LABORATORIO NO EXISTE';
    ELSE
        -- CONSULTAR ALERTAS ACTIVAS (ASUMIENDO QUE NO TENEMOS CAMPO DE ESTADO, USAMOS LAS MÁS RECIENTES)
        SELECT 
            A.ID_ALERTA,
            A.FK_ID_LABORATORIO,
            ML.NOMBRE_LABORATORIO AS MODULO,
            A.MENSAJE_ALERTA,
            TA.NOMBRE_TIPO_ALERTA AS TIPO_ALERTA,
            S.NOMBRE_SENSOR AS SENSOR,
            TS.NOMBRE_TIPO_SENSOR AS TIPO_SENSOR,
            A.CREADA_EN_ALERTA
        FROM ALERTA A
        INNER JOIN LABORATORIO ML ON A.FK_ID_LABORATORIO = ML.ID_LABORATORIO
        INNER JOIN TIPO_ALERTA TA ON A.FK_ID_TIPO_ALERTA = TA.ID_TIPO_ALERTA
        INNER JOIN SENSOR S ON A.FK_ID_SENSOR = S.ID_SENSOR
        INNER JOIN TIPO_SENSOR TS ON S.FK_ID_TIPO_SENSOR = TS.ID_TIPO_SENSOR
        WHERE A.FK_ID_LABORATORIO = P_ID_LABORATORIO
        AND A.CREADA_EN_ALERTA >= DATE_SUB(NOW(), INTERVAL 24 HOUR) -- ALERTAS DE LAS ÚLTIMAS 24 HORAS
        ORDER BY A.CREADA_EN_ALERTA DESC;
    END IF;
END //

